package P36

import (
	"testing"
)

func TestP36(t *testing.T) {
	tests := []struct {
		input Lake
		want  int
	}{
		{
			input: Lake{
				Map: []Square{
					{"w", ".", ".", ".", ".", ".", ".", ".", ".", "w", "w", "."},
					{".", "w", "w", "w", ".", ".", ".", ".", ".", "w", "w", "w"},
					{".", ".", ".", ".", "w", "w", ".", ".", ".", "w", "w", "."},
					{".", ".", ".", ".", ".", ".", ".", ".", ".", "w", "w", "."},
					{".", ".", ".", ".", ".", ".", ".", ".", ".", "w", ".", "."},
					{".", ".", "w", ".", ".", ".", ".", ".", ".", "w", ".", "."},
					{".", "w", ".", "w", ".", ".", ".", ".", ".", "w", "w", "."},
					{"w", ".", ".", ".", "w", ".", ".", ".", ".", ".", "w", "."},
					{".", "w", ".", "w", ".", ".", ".", ".", ".", ".", "w", "."},
					{".", ".", "w", ".", ".", ".", ".", ".", ".", ".", "w", "."},
				},
			},
			want: 3,
		},
	}

	for _, test := range tests {
		result, err := LakeCount(test.input)
		if err != nil {
			t.Fatalf("Should be %d, but %v", test.want, result)
		}

	}
}
